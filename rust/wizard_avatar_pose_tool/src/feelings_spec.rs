#[cfg(test)]
mod tests {
    use super::*;
    use std::collections::BTreeSet;

    #[test]
    fn fifty_unique_sources_are_ordered_after_the_original_catalog() {
        assert_eq!(WJFL_SPECS.len(), 50);
        assert_eq!(
            WJFL_SPECS
                .iter()
                .map(|spec| spec.candidate_id)
                .collect::<BTreeSet<_>>()
                .len(),
            50
        );
        for (index, spec) in WJFL_SPECS.iter().enumerate() {
            assert_eq!(spec.order, index as u32 + 31);
        }
        assert_eq!(
            WJFL_SPECS
                .iter()
                .filter(|spec| matches!(spec.kind, SpecKind::FaceVariant { .. }))
                .count(),
            10
        );
    }
}
