const fn p(x: i32, y: i32) -> Point {
    Point { x, y }
}

#[allow(clippy::too_many_arguments)]
const fn marks(
    mouth: (i32, i32),
    left_eye: (i32, i32),
    right_eye: (i32, i32),
    left_foot: (i32, i32),
    right_foot: (i32, i32),
    left_hand: (i32, i32),
    right_hand: (i32, i32),
    staff_hand: (i32, i32),
    staff_top: (i32, i32),
) -> Landmarks {
    Landmarks {
        mouth: p(mouth.0, mouth.1),
        left_eye: p(left_eye.0, left_eye.1),
        right_eye: p(right_eye.0, right_eye.1),
        left_foot: p(left_foot.0, left_foot.1),
        right_foot: p(right_foot.0, right_foot.1),
        left_hand: p(left_hand.0, left_hand.1),
        right_hand: p(right_hand.0, right_hand.1),
        staff_hand: p(staff_hand.0, staff_hand.1),
        staff_top: p(staff_top.0, staff_top.1),
    }
}

#[allow(clippy::too_many_arguments)]
const fn geometry(
    order: u32,
    candidate_id: &'static str,
    semantic_id: &'static str,
    rows: u32,
    family: MotionFamily,
    contact_mode: ContactMode,
    phase: Option<Phase>,
    effect: bool,
    landmarks: Landmarks,
    neighbors: &'static [&'static str],
) -> PoseSpec {
    PoseSpec {
        order,
        candidate_id,
        semantic_id,
        kind: SpecKind::Geometry,
        generation_rows: Some(rows),
        family,
        contact_mode,
        phase,
        direction: Direction::South,
        view_family: "front",
        effect,
        landmarks,
        neighbors,
    }
}

